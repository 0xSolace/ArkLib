# CHAR-0 vanishing test for n=2^mu via Lam-Leung normal form.
# A multiset M of exponents in Z/n has sum_{a in M} zeta_n^a = 0 in Z[zeta_n]
# (n=2^mu) IFF M decomposes into antipodal pairs {a, a+n/2}. Equivalently: the
# multiplicity vector c in Z^n satisfies c[a] = c[a+n/2] for all a in [0,n/2).
# (Lam-Leung 2000: for n a prime power, minimal vanishing sums are the full set
