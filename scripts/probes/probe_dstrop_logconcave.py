def is_log_concave(seq):
    """seq[i]^2 >= seq[i-1]*seq[i+1] for all interior i (over support, no internal zeros)."""
    s = list(seq)
    # restrict to support hull
    nz = [i for i, v in enumerate(s) if v > 0]
    if len(nz) < 3:
        return True, "trivial (<3 nonzero terms)"
    lo, hi = nz[0], nz[-1]
    # internal zeros break log-concavity of a *positive* sequence's support
    for i in range(lo, hi + 1):
        if s[i] == 0:
            return False, f"internal zero at d={i} (support [{lo},{hi}])"
    bad = []
    for i in range(lo + 1, hi):
        if s[i] * s[i] < s[i - 1] * s[i + 1]:
            bad.append((i, s[i - 1], s[i], s[i + 1]))
    return (len(bad) == 0), (bad if bad else "log-concave on support")

