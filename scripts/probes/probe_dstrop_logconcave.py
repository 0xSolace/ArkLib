def antipodal_closed(S, n):
    h = n // 2
    Sset = set(S)
    return all(((a + h) % n) in Sset for a in Sset)

def p1_char0_zero(S, n):
    """sum_{a in S} zeta_n^a = 0 in Z[zeta_n], n=2^mu (Lam-Leung)."""
    return antipodal_closed(S, n)

def p3_char0_zero(S, n):
    """sum_{a in S} zeta_n^{3a} = 0. gcd(3,n)=1 so a -> 3a mod n is a bijection;
    the image multiset is {3a mod n}; char-0 zero iff that image is antipodal-closed."""
    img = [(3 * a) % n for a in S]
    return antipodal_closed(img, n)

def overdet_bad(S, n):
    """The OVER-DETERMINED char-0 far-line incidence: BOTH p_1 and p_3 vanish."""
    return p1_char0_zero(S, n) and p3_char0_zero(S, n)

