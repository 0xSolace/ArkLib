import numpy as np
import cmath, math
from itertools import product

def primitive_root(p):
    # smallest primitive root mod p
    def order(g):
        x=1
        for k in range(1,p):
            x=(x*g)%p
            if x==1: return k
        return p-1
    for g in range(2,p):
        if order(g)==p-1: return g
    return None

def find_prime_with_mu(n, lo=10):
    # smallest prime p with n | p-1, p > lo
    p = max(lo, n+1)
    while True:
        if (p-1)%n==0 and is_prime(p): return p
        p+=1

def is_prime(m):
    if m<2: return False
    for d in range(2,int(m**0.5)+1):
        if m%d==0: return False
    return True

def mu_n(p, n):
    g = primitive_root(p)
    h = pow(g, (p-1)//n, p)   # primitive n-th root
    return sorted({pow(h, j, p) for j in range(n)})

def ep(p):
    return cmath.exp(2j*math.pi/p)

# ---------------------------------------------------------------------------
print("="*78)
print("(Q1)  Is mu_n a quadratic / Lagrangian orbit?  (theta-series expressibility)")
print("="*78)
# The squares S = {x^2} are the canonical theta orbit: 1_S(y) = (1/2)(1 + chi(y))
# where chi = quadratic character, and sum_y 1_S(y) e_p(by) is a quadratic Gauss sum
# = sqrt(p)-bounded EXACTLY. Question: is mu_n (n=2^a, n | p-1) ALSO such an orbit?
# mu_n = {x : x^n = 1} = ker of the map x -> x^n on F_p^*. It is the IMAGE of x -> x^{(p-1)/n}.
# For it to be a quadratic theta orbit we'd need mu_n = {Q-values} for a quadratic form,
# i.e. mu_n = image of a degree-2 map. But mu_n is the image of a degree-(p-1)/n map.
# Only when (p-1)/n = 2, i.e. n = (p-1)/2, is mu_n = squares = quadratic orbit.
# In the PRIZE regime n ~ p^{1/4}, (p-1)/n ~ p^{3/4} >> 2.  Verify the index:
for (n,) in [(4,),(8,),(16,),(32,)]:
    p = find_prime_with_mu(n, lo=n*n*n+10)  # prize-ish q >> n^3
    idx = (p-1)//n
    print(f"  n={n:3d}  p={p:6d}  index (p-1)/n = {idx:6d}   "
          f"{'QUADRATIC orbit (n=squares)' if idx==2 else 'degree-'+str(idx)+' orbit -- NOT quadratic'}")
print("  => In the prize regime mu_n is a high-degree (p^{3/4}) orbit, NOT a quadratic/")
print("     Lagrangian theta orbit. The Weil rep acts on QUADRATIC orbits (squares); the")
print("     2-power subgroup is not one. (n=squares only at the boundary n=(p-1)/2 ~ p, not p^{1/4}.)")

