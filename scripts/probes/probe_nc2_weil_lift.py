print("="*78)
print("(Q2)  Weil-rep orbit sup-norm of 1_{mu_n}  vs  the quadratic (squares) baseline")
print("="*78)
# The finite Weil representation of SL_2(F_p) on C[F_p] is generated by:
#   F : (F f)(y) = p^{-1/2} sum_x f(x) e_p(xy)         [Fourier, = w = [[0,-1],[1,0]]]
#   M_a: (M_a f)(x) = e_p(a x^2) f(x)                  [unipotent, = [[1,a],[0,1]] up to Weil cst]
#   D_t: (D_t f)(x) = chi(t) f(t^{-1} x)               [diagonal scaling]
# A vector is a "theta vector" (sqrt-bounded sup-norm under the WHOLE orbit) iff it is a
# delta or a Gaussian e_p(a x^2). Test: how does sup|.| evolve along the orbit of 1_{mu_n}?
def fourier(f, p, om):
    n=len(f)
    out=np.zeros(p, dtype=complex)
    for y in range(p):
        s=0
        for x in range(p):
            s+= f[x]*om**((x*y)%p)
        out[y]=s/math.sqrt(p)
    return out

def gaussmult(f, p, a, om):
    return np.array([f[x]*om**((a*x*x)%p) for x in range(p)], dtype=complex)

for (n,) in [(4,),(8,),(16,)]:
    p = find_prime_with_mu(n, lo=n*n+10)   # q ~ n^2 region (keeps p small enough to DFT)
    om = ep(p)
    M = mu_n(p,n)
    f0 = np.zeros(p, dtype=complex);
    for x in M: f0[x]=1.0
    # squares baseline
    sq = sorted({pow(x,2,p) for x in range(1,p)})
    g0 = np.zeros(p, dtype=complex)
    for x in sq: g0[x]=1.0
    # apply a length-4 Weil word F M_1 F M_1 to each and track sup along the way
    def orbit_sup(vec):
        sups=[np.max(np.abs(vec))]
        v=vec
        for a in [1,1,1,1]:
            v=gaussmult(v,p,a,om);
            v=fourier(v,p,om)
            sups.append(np.max(np.abs(v)))
        return sups
    sm = orbit_sup(f0); sg = orbit_sup(g0)
    print(f"  n={n:3d} p={p:5d}  sqrt(n)={math.sqrt(n):.2f}  sqrt(p)={math.sqrt(p):.2f}")
    print(f"     mu_n   sup along Weil orbit: {[f'{s:.2f}' for s in sm]}")
    print(f"     squares sup along Weil orbit:{[f'{s:.2f}' for s in sg]}")
print("  Interpretation: a TRUE theta vector keeps sup ~ O(1) or sqrt(p)/sqrt(p)=1 (the")
print("  Gaussian is a Weil eigenvector). If mu_n's orbit sup BLOWS UP / wanders to ~sqrt(p),")
print("  it is NOT a theta vector and the Weil bound does not apply to it as a whole.")
