import math
import cmath
from collections import defaultdict
import sympy
from sympy import isprime


# ---------------------------------------------------------------------------
# subgroup machinery
# ---------------------------------------------------------------------------
def find_prime(n, mult_lower):
    """Smallest prime p = 1 mod n with p > mult_lower (so we can force p >> n^3)."""
    base = ((mult_lower // n) + 1) * n + 1
    p = base
    while True:
        if isprime(p):
            return p
        p += n


def primitive_root_of_order(p, n):
    g = sympy.primitive_root(p)
    return pow(g, (p - 1) // n, p)


def mu_n(p, n):
    w = primitive_root_of_order(p, n)
    G = [pow(w, i, p) for i in range(n)]
    assert len(set(G)) == n
    return G


def gauss_period(G, b, p):
    """eta_b = Sum_{y in G} e_p(b y)."""
    s = 0j
    for y in G:
        s += cmath.exp(2j * math.pi * (b * y % p) / p)
    return s


def wall_M(G, p):
    """M(mu_n) = max_{b != 0} |eta_b|  (the BGK/Paley wall object)."""
    n = len(G)
    best = 0.0
    argbest = None
    # b ranges over a representative set; by Galois the value depends on the coset
    # of b under mu_n, so it is enough to range over b=1..p-1, but that is huge.
    # Range over a sample plus the structured cosets. For an honest worst-case we
    # need all b; for moderate p we do all b (p is small here).
    for b in range(1, p):
        v = abs(gauss_period(G, b, p))
        if v > best:
            best = v
            argbest = b
    return best, argbest


