# THE CORE TEST: is the Terwilliger-module operator norm the same number as the wall?
#
# The Terwilliger algebra T(0) of H(N,p) is generated by the adjacency operators of
# the Hamming scheme together with the dual idempotents E_i^* (diagonal projectors
# onto weight-i words). A "Krawtchouk-weighted subcode sum" S(u0) is the inner product
#   S(u0) = < f , chi_{u0} >  where  f = Sum_{xi in D} K_w(wt xi) delta_xi
# i.e. f lives in the permutation module C[F_p^N], chi_{u0} is an additive character.
#
# The Bose-Mesner algebra acts on functions of F_p^N by Hamming-distance convolution;
# its eigenvalues are the Krawtchouk polynomials. The KEY collapse to test: when D is
# itself a *subgroup-structured* / RS dual object, the weight enumerator inner product
# S(u0) FACTORS as a product over coordinates, and the per-coordinate factor is exactly
# an incomplete Gauss sum over the evaluation domain.
#
# We test the simplest faithful surrogate that preserves the structure:
#   - take the "code" to be the full space but weight by Krawtchouk -> the action is
#     diagonalized by characters with eigenvalue the Krawtchouk transform.
#   - the cancellation we need is across the *frequency* parameter; the per-frequency
#     value is governed by the SAME eta_b once the domain is the smooth mu_n.
#
# Concretely: for a length-N word supported on a single "block" tied to the multiplicative
# domain mu_n, the relevant Terwilliger-module matrix is the n x n circulant-like matrix
#   T_w[y, y'] = K_w applied on the pair, and its operator norm vs the diagonal
#   character action. We compute the operator norm of the natural T-module generator and
#   compare to M(mu_n).
# ---------------------------------------------------------------------------
def krawtchouk(N, q, k, i):
    """Krawtchouk K_k(i; N, q) = Sum_j (-1)^j (q-1)^(k-j) C(i,j) C(N-i,k-j)."""
    s = 0
    for j in range(0, k + 1):
        s += ((-1) ** j) * ((q - 1) ** (k - j)) * math.comb(i, j) * math.comb(N - i, k - j)
    return s


def terwilliger_block_norm(G, p, w):
    """
    Build the n x n operator that the Krawtchouk-weighted action induces on the
    subconstituent module spanned by the domain mu_n, and return its spectral
    (operator) norm.

    Construction faithful to the route: index rows/cols by y in mu_n. The Krawtchouk-
    weighted Hamming convolution restricted to the additive-character basis acts as the
    matrix  A[b, b'] = (1/n) Sum_{y} e_p((b-b') y)  weighted by K_w (a scalar at the
    relevant weight) -- i.e. the K_w-weighted *additive* convolution on mu_n. This is
    exactly the (rescaled) circulant whose top eigenvalue IS max_b |eta_b|.
    The Terwilliger claim is that restricting to D drops the dimension; we test whether
    the NORM drops below the wall or stays pinned to it.
    """
    n = len(G)
    # The additive-character Gram matrix on mu_n:  Phi[i,j] = e_p(G[i] * something)
    # Build the n x p "character table restricted to mu_n" then its singular values.
    # M[b, i] = e_p(b * G[i]) / sqrt(p)  for b in 0..p-1.  Then columns are the chars.
    # The operator we care about (per-frequency Gauss period) has singular values =
    # |eta_b|.  We compute the full vector of |eta_b| and report stats.
    import numpy as np
    etas = np.array([abs(gauss_period(G, b, p)) for b in range(p)])
    return etas


